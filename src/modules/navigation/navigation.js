    createMapFromItems(items, groupId){
        var map = new Map();
        for (var i=0;i<items.length;i++){
            if (items[i].groupId.includes(groupId)){
                map.set(items[i].path, items[i]);
                items[i].subItems=[];
            }        
        }
        return map;
    },

    addSubItemsToItemsInMap(map, lowerLvlItems, groupId){
        for (var i=0;i<lowerLvlItems.length;i++){
            if (map.has(lowerLvlItems[i].upperLvl) && lowerLvlItems[i].groupId.includes(groupId)){
                var upperLvlItem = map.get(lowerLvlItems[i].upperLvl);
                upperLvlItem.subItems.push(lowerLvlItems[i]);
            }
        }
        return map;
    },

